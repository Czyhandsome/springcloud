/**
 * Zuul网关
 *
 * @author 曹子钰, 2018/3/5
 */
@SpringCloudApplication
@EnableZuulProxy
public class GatewayApplication {
    public static void main(String[] args) {
        SpringApplication.run(GatewayApplication.class, args);
    }
}
